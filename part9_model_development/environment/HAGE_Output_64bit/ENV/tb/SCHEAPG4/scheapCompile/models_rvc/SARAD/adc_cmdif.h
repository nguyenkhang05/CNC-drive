bool str2num(std::string str, bool &num)
{
  std::istringstream is(str);
  int tmp;

  if (str.substr(0, 2) == "0x") {
    is >> std::hex >> tmp;
  }
  else if (str.substr(0, 1) == "0") {
    is >> std::oct >> tmp;
  } else {
    is >> std::dec >> tmp;
  }

  if (is.fail() || !is.eof()) {
    if (str == "true") {
      num = true;
    } else if (str == "false") {
      num = false;
    } else {
      return false;
    }
  } else {
    if (tmp == 1) {
      num = true;
    } else if (tmp == 0) {
      num = false;
    } else {
      return false;
    }
  }
  return true;
}

