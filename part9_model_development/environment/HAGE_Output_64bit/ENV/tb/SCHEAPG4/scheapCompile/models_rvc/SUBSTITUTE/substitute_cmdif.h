template<typename T>
bool str2num(std::string str, T &num)
{
  std::istringstream is(str);
  int tmp;

  if (str.substr(0, 2) == "0x") {
      is >> std::hex >> tmp;
  }
  else if (str.substr(0, 1) == "0") {
      is >> std::oct >> tmp;
  }
  else {
      is >> std::dec >> tmp;
  }

  if (is.fail() || !is.eof()) {
    return false;
  }
  else {
    num = (T)tmp;
    return true;
  }
}

