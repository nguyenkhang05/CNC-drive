/// Mask unselected bit
/// @return selected value
Chwcore_pi_regif::uint Chwcore_pi_regif::bit_select(cuint val,    ///< [in] Writting address
                                                    cuint start,  ///< [in] start bit position
                                                    cuint end)    ///< [in] end bit position
{
    uint ret_val = val;
    if (end - start + 1 < 32) {
        ret_val = (val>>start) & (uint)((1<<(end-start+1))-1);
    }
    return ret_val;
}

